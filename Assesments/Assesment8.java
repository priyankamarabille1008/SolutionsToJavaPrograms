package Assesments;
public class Assesment8 {
    public static int squareNumber(int num){
        return num*num;
    }
    public static int cubeNumber(int num){
        return num*num*num;
    }
    public static void main(String[] args){
        System.out.println(squareNumber(7));
        System.out.println(cubeNumber(3));
    }
}
